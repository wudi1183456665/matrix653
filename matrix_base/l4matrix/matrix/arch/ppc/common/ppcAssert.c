#include <Matrix.h>
/*********************************************************************************************************
** 函数名称: archAssert
** 功能描述: 系统断言.
** 输　入  : iCond          条件
**           pcFunc         调用函数
**           pcFile         所在文件
**           iLine          文件行号
** 输　出  : NONE
** 全局变量:
** 调用模块:
*********************************************************************************************************/
VOID archAssert (INT  iCond, CPCHAR  pcFunc, CPCHAR  pcFile, INT  iLine)
{
	if (!iCond) {
		bspDebugMsg("function: %s() in file: %s line: %d serious error!\n", pcFunc, pcFile, iLine);
		while(1);
	}
}
