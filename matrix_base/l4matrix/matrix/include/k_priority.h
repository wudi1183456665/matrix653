#ifndef __K_PRIORITY_H
#define __K_PRIORITY_H

/*********************************************************************************************************
  任务优先级范围
*********************************************************************************************************/
#define MX_PRIO_HIGHEST         0                                       /*  Matrix 最高优先级           */
#define MX_PRIO_LOWEST          1023                                    /*  Matrix 最低优先级           */
/*********************************************************************************************************
  优先级 (一般应用的最高优先级不能高于 MX_PRIO_CRITICAL 最低不能低过 MX_PRIO_LOW)
*********************************************************************************************************/
#define MX_PRIO_EXTREME         MX_PRIO_HIGHEST                         /*  最高优先级                  */
#define MX_PRIO_CRITICAL        200                                     /*  关键处理任务                */
#define MX_PRIO_REALTIME        400                                     /*  实时处理任务                */
#define MX_PRIO_HIGH            600                                     /*  高优先级任务                */
#define MX_PRIO_NORMAL          800                                     /*  正常优先级                  */
#define MX_PRIO_LOW             1000                                    /*  低优先级                    */
#define MX_PRIO_IDLE            MX_PRIO_LOWEST                          /*  最低优先级                  */
/*********************************************************************************************************
  系统任务优先级
*********************************************************************************************************/
#define MX_PRIO_T_EXCPT         (MX_PRIO_EXTREME)                       /*  t_except 线程               */
#define MX_PRIO_T_TICK          (MX_PRIO_EXTREME + 10)                  /*  t_tick 线程                 */

#endif                                                                  /*  __K_PRIORITY_H              */
