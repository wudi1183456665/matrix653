#ifndef  __K_HEAP_H
#define  __K_HEAP_H

/*********************************************************************************************************
  堆内分段类型
*********************************************************************************************************/
typedef struct {
    UINT                  SEGMENT_uiOwner;                              /*  内存段所有者                */
    ULONG                 SEGMENT_ulLowAddr;                            /*  起始地址                    */
    ULONG                 SEGMENT_ulHigAddr;                            /*  结束地址(包含)              */
} MX_SEGMENT;
typedef MX_SEGMENT       *PMX_SEGMENT;
/*********************************************************************************************************
  堆类型定义
*********************************************************************************************************/
typedef struct {
    MX_SEGMENT            HEAP_segments[MX_CFG_MAX_SEGMENTS];           /*  管理的内存区                */
    UINT                  HEAP_uiValidNum;
} MX_HEAP;
typedef MX_HEAP          *PMX_HEAP;

#endif                                                                  /*  __K_HEAP_H                  */
